  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1); //Starts the PWM signal generation

  HAL_TIM_Base_Start(&htim1); // Start the generation of TIM Base, here used for the ultra_sonic sensor
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);  // We start with set the TRIG pin to low

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);  // set the TRIG pin to HIGH
	  us_delay(10); //wait for 10 us
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);  // set the TRIG pin to low

	  while (!(HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_8)));

	  // wait for the echo pin to go low
	  while ((HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_8))) // Read echo pin
	  {
		  echo_time = __HAL_TIM_GET_COUNTER (&htim1); // get the TIM_1 Counter Register value
	  }

	  Distance = echo_time * 0.034/2; // distance calculation
	  HAL_Delay(50);

	  // We use clock F=45MHz
	  // For clock period of f=50Hz for the servo
	  // we have F/f = 45000000/50 = 900000
	  // Prescaler = 900
	  // Counter period = 1000-1

	  htim2.Instance -> CCR1 = 25; // 2.5% of 1000 => 0.5 ms
	  HAL_Delay(2000);

	  htim2.Instance -> CCR1 = 75; // 7.5% of 1000 => 1.5 ms
	  HAL_Delay(2000);

	  htim2.Instance -> CCR1 = 125; // 12.5% of 1000 => 2.5 ms
	  HAL_Delay(2000);



    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
