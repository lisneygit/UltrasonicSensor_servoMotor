void us_delay (uint16_t us) // Function to create a delay in microseconds
{
  __HAL_TIM_SET_COUNTER(&htim1, 0); //The counter in the TIM1 is set to 0
  while (__HAL_TIM_GET_COUNTER(&htim1) < us); //The counter continues counting, until the us value is reached
}
